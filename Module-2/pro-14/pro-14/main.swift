/* WAP to perform operation like. Union, intersection etc. using set.*/

import Foundation

let a:Set<Int> = [7,3,4,5,6]
let b:Set<Int> = [8,6,5,4,3]

print("a:",a)
print("union of a and b:",a.union(b))

print("b:",b)
print("intersection of a and b:",a.intersection(b))



